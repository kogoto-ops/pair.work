# Get inputs from the user
a = int(input("Enter the first number to add: "))
b = int(input("Enter the second number to add: "))

# Perform addition
print("When you add", a, "and", b, "you get:", a + b)

# Get inputs for subtraction
x = int(input("Enter the number to subtract from: "))
y = int(input("Enter the number to subtract: "))

# Perform subtraction
print("When you subtract", y, "from", x, "you get:", x - y)
